  constructor(private cargoService: CargoService) {}

  ngOnInit() {
    this.loadCargos();
  }

  loadCargos() {
    this.loading = true;
    this.error = null;

    this.cargoService.getAllCargos().subscribe({
      next: (data) => {
        this.cargos = data;
        this.loading = false;
      },
      error: (err) => {
        this.error = 'Error al cargar los cargos';
        this.loading = false;
        console.error('Error al cargar los cargos:', err);
      }
    });
  }

  getRangoBadgeColor(rango: string): string {
    return this.rangoConfig[rango as Rango]?.color || 'bg-gray-100 text-gray-700';
  }

  getRangoIcon(rango: string): string {
    return this.rangoConfig[rango as Rango]?.icon || 'M6 18L18 6M6 6l12 12';
  }
